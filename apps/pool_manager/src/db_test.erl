-module(db_test).
-behaviour(gen_server).

%%% =================================================================
%%% API functions
%%% =================================================================
-export([start_link/0,
        stop/1]).

%%% gen_server callbacks
-export([init/1,
    handle_call/3,
    handle_cast/2,
    handle_info/2,
    terminate/2,
    code_change/3]).

-record(state, {}).

%%% -----------------------------------------------------------------
%%% Starts the server
%%% -----------------------------------------------------------------
start_link() ->
    gen_server:start_link(?MODULE, [], []).

stop(Pid) ->
    gen_server:stop(Pid).

query(Pid, Query) ->   
    gen_server:call(Pid, {query, Query}).

%%% =================================================================
%%% gen_server callbacks
%%% =================================================================

%%% -----------------------------------------------------------------
%%% init/1
%%% -----------------------------------------------------------------
init([]) ->
    {ok, #state{}}.

%%% -----------------------------------------------------------------
%%% handle_call/3
%%% -----------------------------------------------------------------
handle_call({query, Query}, _From, State) ->
    {reply, {ok, Query}, State};
handle_call(_Request, _From, State) ->
    {reply, ok, State}.

%%% -----------------------------------------------------------------
%%% handle_cast/2
%%% -----------------------------------------------------------------
handle_cast(_Request, State) ->
    {noreply, State}.

%%% -----------------------------------------------------------------
%%% handle_info/2
%%% -----------------------------------------------------------------
handle_info(_Info, State) ->
    {noreply, State}.

%%% -----------------------------------------------------------------
%%% terminate/2
%%% -----------------------------------------------------------------
terminate(_Reason, _State) ->
    ok.

%%% -----------------------------------------------------------------
%%% code_change/3
%%% -----------------------------------------------------------------
code_change(_OldVsn, State, _Extra) ->
    {ok, State}.

%%% =================================================================
%%% Internal functions
%%% =================================================================
